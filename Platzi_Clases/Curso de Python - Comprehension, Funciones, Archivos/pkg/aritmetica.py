# Modulo del paquete de la clase de Paquetes

# Funcion para realizar suma de 2 numeros

def suma(a, b):
    return a + b

# Funcion para realizar la resta de 2 numeros

def resta(a, b):
    return a - b

# Funcion para realizar la division de 2 numeros

def division(a, b):
    return a // b

# Funcion para realizar el producto de 2 numeros

def producto(a, b):
    return a * b